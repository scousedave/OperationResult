namespace LawsonOperationResult.DBSupport
{
	/// <summary>	A record range. </summary>
	public struct RecordRange
	{
		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Gets or sets the range number. </summary>
		///
		/// <value>	The range number. </value>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		public int RangeNumber { get; set; }

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Gets or sets the zero-based index of the start. </summary>
		///
		/// <value>	The start index. </value>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		public int StartIndex { get; set; }

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Gets or sets the number of records. </summary>
		///
		/// <value>	The number of records. </value>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		public int RecordCount { get; set; }

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Constructor. </summary>
		///
		/// <param name="startIndex"> 	The start index. </param>
		/// <param name="recordCount">	Number of records. </param>
		/// <param name="rangeNumber">	The range number. </param>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		public RecordRange(int startIndex, int recordCount, int rangeNumber)
		{
			StartIndex = startIndex;
			RecordCount = recordCount;
			RangeNumber = rangeNumber;
		}
	}
}
