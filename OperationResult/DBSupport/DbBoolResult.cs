using System;

namespace LawsonOperationResult.DBSupport
{
	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Encapsulates the result of a database single. </summary>
	/// <seealso cref="T:LawsonOperationResult.DBSupport.DbResult"/>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	public class DbBoolResult :  DbResult
	{
		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Gets or sets the query result. </summary>
		///
		/// <value>	The query result. </value>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		public bool QueryResult { get; set; }

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Constructor. </summary>
		///
		/// <param name="result">	The result. </param>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		public DbBoolResult(bool result)
			: base(result)
		{
			QueryResult = result;
		}

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Constructor. </summary>
		///
		/// <param name="ex">	The ex. </param>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		public DbBoolResult(Exception ex)
			: base(ex)
		{
		}


	}
}
